// Copies canonical packages/rules modules into supabase/functions/_shared so edge functions
// can bundle them - edge function bundles can't reach outside supabase/functions/
// (docs/DECISIONS.md 2026-07-17, same reason settings/model-routing.ts is a mirror). The
// source files use explicit .ts extension imports and zero platform APIs, so they run under
// Deno unchanged.
//
//   node scripts/sync-guide-shared.mjs          # copy all mirrors
//   node scripts/sync-guide-shared.mjs --check  # exit 1 if any mirror is stale
//
// Mirrored: src/guide -> _shared/guide (F04 pipeline), src/state -> _shared/state (Phase 4
// GameState contract). Tests (*.test.ts) and __fixtures__ stay behind.

import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const MIRRORS = ['guide', 'state'].map((name) => ({
  name,
  src: join(root, 'packages', 'rules', 'src', name),
  dest: join(root, 'supabase', 'functions', '_shared', name),
}))
const HEADER = '// GENERATED by scripts/sync-guide-shared.mjs - DO NOT EDIT.\n// Canonical source: packages/rules/src/. Edit there, then re-run the script.\n\n'

function listSourceFiles(dir) {
  const files = []
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === '__fixtures__' || entry.name.endsWith('.test.ts')) continue
    const path = join(dir, entry.name)
    if (entry.isDirectory()) files.push(...listSourceFiles(path))
    else if (entry.name.endsWith('.ts')) files.push(path)
  }
  return files
}

const checkOnly = process.argv.includes('--check')
let anyStale = false

for (const { name, src, dest } of MIRRORS) {
  const expected = new Map(
    listSourceFiles(src).map((path) => [relative(src, path).replaceAll('\\', '/'), HEADER + readFileSync(path, 'utf8')]),
  )

  if (checkOnly) {
    const actual = new Map(
      existsSync(dest)
        ? listSourceFiles(dest).map((path) => [relative(dest, path).replaceAll('\\', '/'), readFileSync(path, 'utf8')])
        : [],
    )
    const stale =
      actual.size !== expected.size ||
      [...expected].some(([rel, content]) => actual.get(rel) !== content)
    if (stale) {
      console.error(`supabase/functions/_shared/${name} is stale - run: node scripts/sync-guide-shared.mjs`)
      anyStale = true
    } else {
      console.log(`${name} mirror is up to date`)
    }
    continue
  }

  rmSync(dest, { recursive: true, force: true })
  for (const [rel, content] of expected) {
    const target = join(dest, rel)
    mkdirSync(dirname(target), { recursive: true })
    writeFileSync(target, content)
  }
  console.log(`synced ${expected.size} files to supabase/functions/_shared/${name}`)
}

if (checkOnly && anyStale) process.exit(1)
